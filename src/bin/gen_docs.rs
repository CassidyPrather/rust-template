//! Documentation generator.
//!
//! Regenerates the Usage section of `README.md` (between the
//! `generated-usage` markers) and all of `SKILL.md` from the clap
//! definition, so the committed docs never drift from the actual `--help`
//! output. Run with:
//!
//! ```text
//! cargo run --bin gen-docs
//! ```
//!
//! CI runs it and fails if the committed files differ from the output.

use std::fmt::Write as _;
use std::fs;
use std::path::Path;

use anyhow::{Context, Result, ensure};
use clap::CommandFactory;

use rust_template::cli::Cli;

const README_START: &str = "<!-- generated-usage:start -->";
const README_END: &str = "<!-- generated-usage:end -->";

fn main() -> Result<()> {
    let root = Path::new(env!("CARGO_MANIFEST_DIR"));
    let mut cmd = Cli::command();
    cmd.build();

    let readme_path = root.join("README.md");
    update_readme(&readme_path, &render_usage(&mut cmd)?)?;
    println!("Updated {}", readme_path.display());

    let skill_path = root.join("SKILL.md");
    fs::write(&skill_path, render_skill(&mut cmd)?)
        .with_context(|| format!("failed to write {}", skill_path.display()))?;
    println!("Updated {}", skill_path.display());

    Ok(())
}

/// Render the top-level help text as a fenced code block.
fn render_usage(cmd: &mut clap::Command) -> Result<String> {
    let mut usage = String::new();
    writeln!(usage, "```text")?;
    write!(usage, "{}", cmd.render_long_help())?;
    writeln!(usage, "```")?;
    Ok(usage)
}

/// Render `SKILL.md`: top-level help plus a section per subcommand.
fn render_skill(cmd: &mut clap::Command) -> Result<String> {
    let name = cmd.get_name().to_string();
    let description = cmd.get_about().map(ToString::to_string).unwrap_or_default();

    let mut skill = String::new();
    writeln!(skill, "---")?;
    writeln!(skill, "name: {name}")?;
    writeln!(skill, "description: {description}")?;
    writeln!(skill, "---")?;
    writeln!(skill)?;
    writeln!(skill, "# {name}")?;
    writeln!(skill)?;
    writeln!(
        skill,
        "<!-- Generated by `cargo run --bin gen-docs`. Do not edit by hand. -->"
    )?;
    writeln!(skill)?;
    writeln!(skill, "{}", render_usage(cmd)?)?;

    let subcommands: Vec<String> = cmd
        .get_subcommands()
        .filter(|sub| sub.get_name() != "help")
        .map(|sub| sub.get_name().to_string())
        .collect();
    for sub_name in subcommands {
        let sub = cmd
            .find_subcommand_mut(&sub_name)
            .with_context(|| format!("subcommand {sub_name} vanished mid-render"))?;
        writeln!(skill, "## {name} {sub_name}")?;
        writeln!(skill)?;
        writeln!(skill, "```text")?;
        write!(skill, "{}", sub.render_long_help())?;
        writeln!(skill, "```")?;
        writeln!(skill)?;
    }

    Ok(skill)
}

/// Splice the rendered usage block between the README's markers.
fn update_readme(path: &Path, usage: &str) -> Result<()> {
    let content =
        fs::read_to_string(path).with_context(|| format!("failed to read {}", path.display()))?;
    let start = content
        .find(README_START)
        .with_context(|| format!("{README_START} marker missing from README"))?;
    let end = content
        .find(README_END)
        .with_context(|| format!("{README_END} marker missing from README"))?;
    ensure!(start < end, "README markers are out of order");

    let before = &content[..start + README_START.len()];
    let after = &content[end..];
    let updated = format!("{before}\n\n{usage}\n{after}");
    fs::write(path, updated).with_context(|| format!("failed to write {}", path.display()))?;
    Ok(())
}
